class Translator:
    """Very small translation helper for desktop apps."""

    def __init__(self, language: str = DEFAULT_LANG):
        self.language = self._normalize(language)

    def _normalize(self, language: str | None) -> str:
        if not language:
            return DEFAULT_LANG
        return language if language in SUPPORTED_LANGS else DEFAULT_LANG

    def set_language(self, language: str) -> None:
        self.language = self._normalize(language)

    def t(self, key: str, **kwargs) -> str:
        table = TRANSLATIONS.get(self.language, TRANSLATIONS[DEFAULT_LANG])
        fallback = TRANSLATIONS[DEFAULT_LANG]
        text = table.get(key, fallback.get(key, key))
        if kwargs:
            try:
                return text.format(**kwargs)
            except Exception:
                return text
        return text
